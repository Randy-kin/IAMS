    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String UserName = jTextField1.getText();
        String email = jTextField5.getText();
        String passw = String.valueOf(jTextField4.getText());
        String conpass = String.valueOf(jTextField3.getText());
        
        if (UserName.isEmpty() || email.isEmpty() || passw.isEmpty() || conpass.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter all fields",
                    "Try again",
                    JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        
         if (!passw.equals(conpass)) {
            JOptionPane.showMessageDialog(this,
                    "Confirm Password do not macth",
                    "Try again",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         try {
             
             Statement s = db.mycon().createStatement();
              s.executeUpdate("INSERT INTO user(Name,Email,Password) VALUES('"+UserName+"','"+email+"','"+passw+"')");
               JOptionPane.showMessageDialog(null,"Data Saved");
                openHomePage();
             
         }catch(Exception e){
              System.out.println(e);
         }
        jTextField1.setText("");
        jTextField5.setText("");
        jTextField4.setText("");
        jTextField3.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

